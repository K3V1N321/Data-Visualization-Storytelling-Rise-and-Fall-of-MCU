function introduction() {
    return (
        <>
            <p>
                For more than a decade, the Marvel Cinematic Universe (MCU) dominated blockbuster cinema.
                From the release of <cite>Iron Man</cite> in 2008 to <cite>Avengers: Endgame</cite> in 2019, Marvel built an interconnected franchise that became both financially successful and culturally influential.
                However, in recent years, many audiences have started asking: "Is the MCU falling off?"
                Discussions often mention box office numbers, inconsistent ratings, and "superhero fatigue," but these discussions are often based on opinion rather than data.
                To answer why it feels like the MCU has been falling off and if this feeling is supported by concrete data, we will be analyzing the MCU during their rise and then during their fall.
            </p>
            <p style = {{marginTop: "12px"}}>
                The rise of the MCU occurred during the release of Phase 1 through Phase 3 movies, from 2008 to 2019, reaching its peak in 2019.
                The fall of the MCU has been ongoing since the start of Phase 4 in 2021.
                To explain why the MCU was so sucessful, we will analyze financial and structural trends during its rise.
                Then, we will explain why the MCU has been falling by analyzing the trends of recent movies.
            </p>
        </>
    )
}

function connectionsNarration() {
    return (
        <>
            <p>
                One of the main attractions of MCU movies is their interconnected universe.
                The timeline above displays connections between all MCU movies that have been released.
                The timeline shows 3 types of connections:
            </p>
            <ul style = {{marginTop: "12px"}}>
                <li>Direct sequel: The next movie in a series that continues the story of the previous movie.</li>
                <li>Crossover: A movie whose main characters are the main characters of different movie series.</li>
                <li>Carryover: A movie whose story is directly incfluenced by or continues the story of a previous movie from a different series.</li>
            </ul>
            <p style = {{marginTop: "12px"}}>
                The timeline clearly shows that there are many connections between movies in Phase 1 to Phase 3, highlighting how tightly linked the movies are.
                The usual type of connection, seen in all types of movies, is a direct sequel.
                The appeal of a movie sequel is that it continues the story of the previous movie(s) in the series, which is usually effective in bringing back audience members who enjoyed the previous movie(s).
                Throughout Phase 1 to Phase 3, Marvel created multiple movie series for popular superheroes, like the Iron Man, Captain America, and Thor movies.
                By creating sequels for these series, Marvel was able to continuously release movies that starred popular superheros.
                This was an effective strategy to attract fans to watch movies, since regardless of the story or other aspects of the movie, the superhero being focused on is already a main attraction for many fans.
            </p>
            <p style = {{marginTop: "12px"}}>
                The movie connections that make MCU movies stand out from other movies are the crossover and carryover connections.
                With most movies, their stories are usually told in 1 movie or throughout a movie series, which is typically about 3 movies.
                Rather than only telling a story throughout a series of movies, Marvel told 1 large overarching story throughout Phase 1 to Phase 3, which consisted of 21 movies.
                Telling 1 story throughout all these movies helped create anticipation among fans for every new movie, since each movie was developing the overarching story and building up to the climax.
                The crossover movies, which are the series of Avengers movies and <cite>Captain America: Civil War</cite>, were highlights of the MCU movies.
                This is because these movies not only starred multiple popular superheroes, but they were also the ones that focused the most on the overarching story.
            </p>

        </>
    )
}

